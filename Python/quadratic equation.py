''' Назначение:
решение квадратных уравнений.

Овчинникова Анастасия

Переменные:
a - коэффицент квадратного уравнения при x^2
b - коэффицент квадратного уравнения при x
c - свободный коэффицент квадратного уравнения
D - дискриминант квадратного уравнения
x1 - первый корень квадратнорго уравнения
x2 - второй корень квадратного уравнения
x - единственный кореньквадратного уравнения'''

print('Введите коэффиценты квадратного уравнения ax^2 + bx + c = 0: ')
a = float(input('a = '))
b = float(input('b = '))
c = float(input('c = '))

if a == 0:
    if b != 0:
        x = round(-c/b)
        print ('\nx = ', x)
    else:
        if c == 0:
            print ('\nx ∈ (-∞; +∞)')
        else:
            print ('\nРешений нет.')
else:
    D = b**2 - 4*a*c
    if D > 0:
        x1 = (-b+D**(1/2))/(2*a)
        x2 = (-b-D**(1/2))/(2*a)
        print ('\nx1 = ', x1)
        print ('x2 = ', x2)
    elif D < 0:
        print ('\nНет действительных корней.')
    elif D == 0:
        x = (-b+D**(1/2))/(2*a)
        print ('\nx = ', x)
    
